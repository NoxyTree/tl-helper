// Re-vendors @supabase/supabase-js as a single, pinned, same-origin ES module
// at web/vendor/supabase/supabase.esm.js.
//
//   node scripts/vendor-supabase.mjs
//
// The auth library is self-hosted on purpose (see docs/supabase-setup.md): it
// keeps auth same-origin (CSP-ready with `script-src 'self'`), works offline,
// and carries no runtime CDN trust — the bundle is frozen at commit time. It is
// built LOCALLY from a pinned npm version (not downloaded pre-bundled) so the
// supply-chain surface is "pinned npm version + a reviewable diff".
//
// To update: bump the versions below, re-run, review the diff, commit.
import { execSync } from "node:child_process";
import { mkdtempSync, writeFileSync, mkdirSync, readFileSync, statSync, rmSync } from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SUPABASE_VERSION = "2.110.6";
const ESBUILD_VERSION = "0.28.1";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outDir = path.join(repoRoot, "web", "vendor", "supabase");
const outFile = path.join(outDir, "supabase.esm.js");

const header = `// VENDORED — do not edit by hand. Generated by scripts/vendor-supabase.mjs.
// @supabase/supabase-js@${SUPABASE_VERSION}, bundled with esbuild@${ESBUILD_VERSION} (browser/esm/minified).
// Self-hosted (same-origin) on purpose: CSP-ready, offline-capable, no runtime CDN trust.
`;

const work = mkdtempSync(path.join(tmpdir(), "vendor-supabase-"));
try {
  writeFileSync(path.join(work, "package.json"), JSON.stringify({ name: "vendor-supabase", private: true, type: "module" }));
  // Re-export only what the browser bootstrap consumes.
  writeFileSync(path.join(work, "entry.mjs"), "export { createClient } from '@supabase/supabase-js';\n");

  console.log(`Installing @supabase/supabase-js@${SUPABASE_VERSION} …`);
  execSync(`npm install @supabase/supabase-js@${SUPABASE_VERSION} --no-audit --no-fund --loglevel=error`, { cwd: work, stdio: "inherit" });

  console.log(`Bundling with esbuild@${ESBUILD_VERSION} …`);
  execSync(`npx --yes esbuild@${ESBUILD_VERSION} entry.mjs --bundle --format=esm --minify --platform=browser --target=es2022 --outfile=supabase.bundle.js`, { cwd: work, stdio: "inherit" });

  mkdirSync(outDir, { recursive: true });
  writeFileSync(outFile, header + readFileSync(path.join(work, "supabase.bundle.js"), "utf8"));
  console.log(`\nWrote ${path.relative(repoRoot, outFile)} (${(statSync(outFile).size / 1024).toFixed(0)} KB)`);
} finally {
  rmSync(work, { recursive: true, force: true });
}
